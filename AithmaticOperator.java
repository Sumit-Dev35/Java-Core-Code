public class AithmaticOperator {
  public static void main(String[] args) {
      int a=8;
      int b=5;
      System.out.println(a+b);
      System.out.println(a-b);
      System.out.println(a*b);
      System.out.println(a/b);

      double x=2.5;
      double y=7.6;
      System.out.println(x+y);
      System.out.println(x-y);
      System.out.println(x*y);
      System.out.println(x/y);
  }
}
