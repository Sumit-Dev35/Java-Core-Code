public class RelationalOperator {
  public static void main(String[] args) {
    System.out.println("Hello World");
    int a=5;
    int y=6;
    System.out.println(a==y);
    System.out.println(a!=y);
    System.out.println(a<y);
    System.out.println(a>y);
    System.out.println(a>=y);
    System.out.println(a<=y);
  }
}
